package com.huayu.model;

public class HelperModel {
	private Integer id;
	private String resume;
	private String docLaw;
	private String contactInfo;
	private String userLaw;
	private String helperType;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getResume() {
		return resume;
	}

	public void setResume(String resume) {
		this.resume = resume;
	}

	public String getDocLaw() {
		return docLaw;
	}

	public void setDocLaw(String docLaw) {
		this.docLaw = docLaw;
	}

	public String getContactInfo() {
		return contactInfo;
	}

	public void setContactInfo(String contactInfo) {
		this.contactInfo = contactInfo;
	}

	public String getUserLaw() {
		return userLaw;
	}

	public void setUserLaw(String userLaw) {
		this.userLaw = userLaw;
	}

	public String getHelperType() {
		return helperType;
	}

	public void setHelperType(String helperType) {
		this.helperType = helperType;
	}
}
